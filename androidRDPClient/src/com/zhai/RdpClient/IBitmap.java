package com.zhai.RdpClient;

import android.graphics.Bitmap;

/**
 * Common interface to abstract (wrap) Bitmap/Image or whatever class is used to represent an image
 * @author aromeu
 *
 */
public interface IBitmap {
	
	/**
	 * Returns the Bitmap/Image. The casting has to be made in the implementations
	 * @return
	 */
	public Object getBitmap();
	
	/**
	 * Clears the Bitmap. This is specifically used on Android @see android.graphics.Bitmap.recyclye()
	 */
	public void recycle();

	public Bitmap getBitmap(boolean b);

}
